#include <stdio.h>
#include "main.h"

/**
 * add - this function adds
 * @a: first argument
 * @b: second argument
 *
 * Return: returns an integer
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - this function subs
 * @a: first argument
 * @b: second argument
 *
 * Return: returns an integer
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - this function muls
 * @a: first argument
 * @b: second argument
 *
 * Return: returns an integer
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - this function divs
 * @a: first argument
 * @b: second argument
 *
 * Return: returns an integer
 */

int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - this function mods
 * @a: first argument
 * @b: second argument
 *
 * Return: returns an integer
 */

int mod(int a, int b)
{
	return (a % b);
}
